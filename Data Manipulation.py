#LDA Does single word appearances so not very helpful
def LDA_ANALYSIS(papers , forum, language):
    papers = papers.to_frame(name='Comment')
    papers['paper_text_processed'] = papers['Comment'].map(lambda x: x.lower())
    def sent_to_words(sentences):
        for sentence in sentences:
            yield (gensim.utils.simple_preprocess(str(sentence), deacc=True))
    def remove_stopwords(texts):
        return [[word for word in simple_preprocess(str(doc))if word not in stop_words] for doc in texts]
    data = papers.paper_text_processed.values.tolist()
    data_words = list(sent_to_words(data))
    data_words = remove_stopwords(data_words)
    id2word = corpora.Dictionary(data_words)
    texts = data_words
    corpus = [id2word.doc2bow(text) for text in texts]
    num_topics = 10
    lda_model = gensim.models.LdaMulticore(corpus=corpus, id2word=id2word, num_topics=num_topics)
    #doc_lda = lda_model[corpus]
    LDAvis_data_filepath = os.path.join('TF/' + str(forum+' x '+ language))
    if 1 == 1:
        LDAvis_prepared = gensimvis.prepare(lda_model, corpus, id2word)
        with open(LDAvis_data_filepath, 'wb') as f:
            pickle.dump(LDAvis_prepared, f)
    with open(LDAvis_data_filepath, 'rb') as f:
        LDAvis_prepared = pickle.load(f)
    pyLDAvis.save_html(LDAvis_prepared, 'TF/' + str(forum+' x '+ language) + '.html')
    print('done')
